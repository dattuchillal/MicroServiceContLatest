package com.cg.contService.domain;

public class CustomGetResult {
	
	private String codigo;
	private String cod_socieded;
	private long ts;
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getCod_socieded() {
		return cod_socieded;
	}
	public void setCod_socieded(String cod_socieded) {
		this.cod_socieded = cod_socieded;
	}
	public long getTs() {
		return ts;
	}
	public void setTs(long ts) {
		this.ts = ts;
	}
	

}
